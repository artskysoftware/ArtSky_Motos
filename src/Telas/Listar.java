           List<Moto> listaMoto = dao.ListarCadastro();
           
           DefaultTableModel model = (DefaultTableModel)tabela.getModel();
           model.setNumRows(0);
           
           
           for (Moto v :listaMoto){
               model.addRow(new Object[]{
               v.getCodMoto(),
               v.getCodMarca(),
               v.getCodModelo(),
               v.getKmRodado(),
               v.getPreco(),
               v.getPeso(),
                       
                       });
               
           }   
